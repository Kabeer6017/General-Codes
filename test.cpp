#include <iostream>
using namespace std;
int main(){
    int sum =0; int n;
    float avg=0.0; int i=0;
    while(1==1){
        cin>>n;
        sum = sum +n;
        if(n==0){
            break;
        }
        i++;}
        avg = sum/i;
        cout<<"the average of the series is "<<avg;
        return 0;
}